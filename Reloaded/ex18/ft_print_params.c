#include <stdlib.h>
#include <unistd.h>

void	ft_putchar(char l);

int	main(int ac, char **av)
{
	int i;
	int j;
	
	i = 1;
	while (ac >= 2 && av[i])
	{
		j = 0;
		while (av[i][j])
		{
			ft_putchar(av[i][j]);
			j++;
		}
		ft_putchar('\n');
		i++;
	}
}